  <p>3- En cas de propagation significative du Covid-19 chez les joueurs, la saison de NBA, qui doit reprendre le 30 juillet dans la bulle de Disney World, serait à nouveau menacée, a prévenu mardi le patron de la ligue Adam Silver
  Il est certain que si nous avons une quelconque propagation significative au sein de notre campus, il nous faudrait arrêter à nouveau, a déclaré Adam Silver lors d’une conférence virtuelle du magazine américain.

Alors que les équipes de NBA se rassemblent à Orlando en Floride, en vue de reprendre la saison, Silver a déclaré qu’il s’attendait à ce que certains joueurs soient testés positifs au Covid-19. Mais ce qui serait inquiétant selon lui serait de voir des joueurs testés positifs une fois placés en quarantaine, ce qui signifierait qu’il y a un trou dans notre bulle normalement sécurisée du complexe de Disney World, où doivent s’affronter les 22 équipes NBA pour une place en play-offs.

Le championnat, interrompu depuis le 11 mars, consécutivement à l’annonce du test positif au coronavirus de Rudy Gobert (Utah) reprendra comme un symbole le 30 juillet par un match du Jazz et de son pivot français qui se mesureront à La Nouvelle-Orléans du phénomène Zion Williamson, avant un gros choc le soir même entre les Lakers de LeBron James et les Clippers de Kawhi Leonard.
Des mesures de dépistage du coronavirus et de suivi des contacts seront mises en place, ce qui, espère Silver, rendra le campus sûr pour les joueurs et le personnel, malgré l’augmentation des cas de Covid-19 en Floride. Après les 351 premiers tests effectués depuis le 23 juin, 25 joueurs avaient été testés positifs, avait révélé la NBA le 2 juillet. Dix membres du personnel des clubs l’avaient aussi été.
