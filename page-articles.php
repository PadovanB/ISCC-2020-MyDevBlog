  <p>5- Cette rencontre de saison régulière sera reprogrammée à une date ultérieure, a indiqué l’instance.

« La décision a été prise par respect envers les Lakers, profondément affligés par la perte tragique de la légende du club, Kobe Bryant, sa fille Gianna et sept autres personnes dans un accident d’hélicoptère dimanche », explique la NBA dans un court communiqué.

« Les Lakers de Los Angeles tiennent à vous remercier tous pour la formidable vague de soutien et de condoléances. C’est une période très difficile pour nous tous. Nous continuons à soutenir la famille Bryant et partagerons plus d’informations dès qu’elles seront disponibles », a réagi le club californien dans un communiqué distinct, confirmant la décision de report.

La direction de la franchise, club de toujours de celui qui restera comme un des plus grands joueurs de l’histoire du ballon orange, était jusqu’ici restée silencieuse, encore sous le choc après la tragédie survenue la veille.  

La star de 41 ans, sa fille Gianna âgée de 13 ans, ainsi que sept autres personnes sont mortes dans le crash d’un hélicoptère sur une colline près de Los Angeles.

Alors que toute la planète basket a exprimé sa peine et a rendu hommage à Bryant, la star actuelle des Lakers LeBron James ne s’est pas encore exprimée. Des images l’ont montré en larmes dimanche après-midi sur le tarmac de l’aéroport de Los Angeles, où son équipe venait d’atterrir en provenance de Philadelphie.

Le premier joueur de l’équipe à avoir pris la parole est l’ailier Anthony Davis.

« Que c’est dur. Tu as été le premier gars à me prendre sous ton aile et à me montrer les rouages de la ligue. On a eu tellement de bonnes conversations sur tant de sujets et je chérirai ces moments pour toujours. Je t’aime pour toujours, #RIPKobe #RIPGiGi », a-t-il écrit lundi sur instagram.


